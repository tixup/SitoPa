          <h3 id="studio-title">Storia</h3>
        <p>Laureato con Prof. Arch. Giuliano Forno nel 1995 in architettura all’Università degli Studi di Genova sviluppando una tesi di Progettazione Urbanistica dal titolo: “La Magra: proposte di intervento per una riqualificazione urbanistico-ambientale delle sponde nelle vicinanze di Sarzana”. Iscritto all’ordine degli architetti della Provincia della Spezia al n. 252, nell’Albo Regionale degli Esperti in materia di bellezze naturali ed architettoniche dal 1999 e inserito nell’Elenco collaudatori del cemento armato Legge 1086 del 5.11.1971. Collabora da subito con diverse Amministrazioni pubbliche della Val di Vara (La Spezia) in qualità di Responsabile e/o consulente dell’Area Tecnica in qualità di Istruttore Direttivo.</p>

        <p>Studi e approfondimenti in materia ambientale hanno consentito l’ottenimento del Diploma di Tecnico ANAB-IBN in Architettura Bioecologica. Nel 2008 apre il proprio studio di architettura e urbanistica mtaa.</p>

        <p>Fondato nel 2008 da Marco Tognetti, marcotognettiarchitetto è uno studio di progettazione architettonica e urbanistica con sede a Brugnato, un piccolo paese della verde Val di Vara nell’entroterra della Provincia della Spezia. Ci occupiamo di servizi di architettura e urbanistica, opere pubbliche, restauro, interior, produttivo e commerciale grazie alle diverse competenze maturate negli anni in ambito architettonico, di arredo, nell’architettura rurale, urbana e nella pianificazione territoriale. Ciò permette di gestire il progetto in tutta la sua interezza, dalla scala urbana fino alla scala architettonica. Partendo dalla ricerca delle atmosfere di un luogo o spazio, con la storia e la materia di cui sono composti, progettiamo il desiderio dell'abitare e di insediare le attività dell’uomo, valorizzando così l'identità e la cultura del nostro territorio.</p>
